import java.util.*;
public class divisors{
    public static void main(String args[]){
        Scanner s=new Scanner(System.in);
        int t=s.nextInt();
        for(int i=0;i<t;i++){
            int n=s.nextInt();
            for(int j=0;j<=n;j++){
                System.out.print(j+" ");
            }
        }System.out.println();
    }
}
        